package com.example.guihuan.chatwifitest;



public class Msg {

    private String name;
    private int imageId;

    public Msg(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}
